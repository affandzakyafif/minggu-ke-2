function luasbangunan (panjang,lebar,tinggi){
    document.write('panajng'+':'+panjang)
    document.write("<br>")
    document.write('lebar'+':'+lebar)
    document.write("<br>")
    document.write('tinggi'+':'+tinggi)
    document.write("<br>")
    document.write('luas bangunan'+':'+ panjang*lebar*tinggi)
}

luasbangunan(8,9,7)